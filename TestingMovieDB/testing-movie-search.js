async function getJsonAPI(movieId) {
  const options = {
    method: "GET",
    headers: {
      accept: "application/json",
      Authorization:
        // ENTER AUTHORIZATION CODE HERE
        "ENTER CREDENTIALS HERE",
    },
  };

  const response = await fetch(
    `https://api.themoviedb.org/3/search/movie?query=${movieId}&include_adult=false&language=en-US&page=1`,
    options
  );

  if (!response.ok) {
    throw new Error(`HTTPerror:${response.status}`);
  }

  const data = await response.json();
  //   main.innerHTML += data.results[0].title;
  data.results.forEach((element) => {
    main.innerHTML += `<p>${element.title}</p>`;
  });
}
